    private void iniciarPlanificacionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarPlanificacionesActionPerformed
        
        if(modelSolicitudes.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "No has agregado ninguna solicitud");
        }else if(modelSolicitudes.contains(Integer.parseInt(jFormattedTextFieldCabeza.getText()))){
            JOptionPane.showMessageDialog(rootPane, "La cabeza no puede ser parte de las solicitudes");
        }else{
            int cabeza = Integer.parseInt(jFormattedTextFieldCabeza.getText());
            ArrayList<Integer> solicitudes = new ArrayList<>();
            solicitudes.add(cabeza);//la cabeza es el primer elemento de la lista

            //JOptionPane.showMessageDialog(this, modelSolicitudes.size());
            for(int i = 0;i < modelSolicitudes.size();i++){
                //String j = String.valueOf(jListSolicitudes.getModel().getElementAt(i));
                solicitudes.add(Integer.parseInt(String.valueOf(jListSolicitudes.getModel().getElementAt(i))));
            }        

            MostrarPlanificacion plan= new MostrarPlanificacion(cabeza,this.direccion,solicitudes);
            plan.setVisible(true);
            this.setVisible(false);
        }
         
    }//GEN-LAST:event_iniciarPlanificacionesActionPerformed

