    private void jButtonAgregarSolicitudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarSolicitudActionPerformed
        // TODO add your handling code here:
        //JOptionPane.showMessageDialog(this, this.direccion);
        //int cabeza = Integer.parseInt(jFormattedTextFieldCabeza.getText());
        //JOptionPane.showInternalMessageDialog(this, cabeza);
        int intSolicitud;String stringSolicitud = "";
        stringSolicitud = JOptionPane.showInputDialog(this, "Ingrese Solicitud", "Numero entereo").trim();
        try {
            intSolicitud = Integer.parseInt(stringSolicitud);
            if(!modelSolicitudes.contains(intSolicitud)&&intSolicitud != Integer.parseInt(jFormattedTextFieldCabeza.getText()))//No permite repetidos ni el valor de la cabeza
                modelSolicitudes.addElement(Math.abs(intSolicitud));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error: Ingrese una solicitud valida");
        }
    }//GEN-LAST:event_jButtonAgregarSolicitudActionPerformed

